<template>
	<div id="container"></div>
</template>
<script src="https://cdn.bootcss.com/three.js/r83/three.min.js">
	import * as THREE from 'three'

	import nx from './assets/nx.jpg'
	import ny from './assets/ny.jpg'
	import nz from './assets/nz.jpg'
	import px from './assets/px.jpg'
	import py from './assets/py.jpg'
	import pz from './assets/pz.jpg'
	
	var camera, scene, renderer;
	var texture_placeholder,
	isUserInteracting = false,
	onMouseDownMouseX = 0, onMouseDownMouseY = 0,
	lon = 90, onMouseDownLon = 0,
	lat = 0, onMouseDownLat = 0,
	phi = 0, theta = 0,
	target = new THREE.Vector3();
	export default {
	 	mounted(){
	 		this.init();
			this.animate();
	 	},
	 	methods:{
	 		init:function(){
	 			var container, mesh;
				container = document.getElementById( 'container' );
				camera = new THREE.PerspectiveCamera( 75, window.innerWidth / window.innerHeight, 1, 1100 );
				scene = new THREE.Scene();
				texture_placeholder = document.createElement( 'canvas' );
				texture_placeholder.width = 128;
				texture_placeholder.height = 128;
				var context = texture_placeholder.getContext( '2d' );
				context.fillStyle = 'rgb( 200, 200, 200 )';
				context.fillRect( 0, 0, texture_placeholder.width, texture_placeholder.height );
				var materials = [
					this.loadTexture( px ), // right
					this.loadTexture( nx), // left
					this.loadTexture(py ), // top
					this.loadTexture(ny ), // bottom
					this.loadTexture( pz ), // back
					this.loadTexture( nz )  // front
				];
				mesh = new THREE.Mesh( new THREE.BoxGeometry( 300, 300, 300, 7, 7, 7 ), materials );
				mesh.scale.x = - 1;
				scene.add( mesh );
				renderer = new THREE.CanvasRenderer();
				renderer.setPixelRatio( window.devicePixelRatio );
				renderer.setSize( window.innerWidth, window.innerHeight );
				container.appendChild( renderer.domElement );
				document.addEventListener( 'mousedown', onDocumentMouseDown, false );
				document.addEventListener( 'mousemove', onDocumentMouseMove, false );
				document.addEventListener( 'mouseup', onDocumentMouseUp, false );
				document.addEventListener( 'wheel', onDocumentMouseWheel, false );
				document.addEventListener( 'touchstart', onDocumentTouchStart, false );
				document.addEventListener( 'touchmove', onDocumentTouchMove, false );
				//
				window.addEventListener( 'resize', onWindowResize, false );
	 		},
	 		onWindowResize:function(){
	 			camera.aspect = window.innerWidth / window.innerHeight;
				camera.updateProjectionMatrix();
				renderer.setSize( window.innerWidth, window.innerHeight );
	 		},
	 		loadTexture:function(path){
	 			var texture = new THREE.Texture( texture_placeholder );
				var material = new THREE.MeshBasicMaterial( { map: texture, overdraw: 0.5 } );
				var image = new Image();
				image.onload = function () {
					texture.image = this;
					texture.needsUpdate = true;
				};
				image.src = path;
				return material;
	 		},
	 		onDocumentMouseDown:function(event){
	 			event.preventDefault();
				isUserInteracting = true;
				onPointerDownPointerX = event.clientX;
				onPointerDownPointerY = event.clientY;
				onPointerDownLon = lon;
				onPointerDownLat = lat;
	 		},
	 		onDocumentMouseMove:function(event){
	 			if ( isUserInteracting === true ) {
					lon = ( onPointerDownPointerX - event.clientX ) * 0.1 + onPointerDownLon;
					lat = ( event.clientY - onPointerDownPointerY ) * 0.1 + onPointerDownLat;
				}

	 		},
	 		onDocumentMouseUp:function(event){
	 			isUserInteracting = false;
	 		},
	 		onDocumentMouseWheel:function(event){
	 			var fov = camera.fov + event.deltaY * 0.05;
				camera.fov = THREE.Math.clamp( fov, 10, 75 );
				camera.updateProjectionMatrix();
	 		},
	 		onDocumentTouchStart:function(event){
	 			if ( event.touches.length == 1 ) {
					event.preventDefault();
					onPointerDownPointerX = event.touches[ 0 ].pageX;
					onPointerDownPointerY = event.touches[ 0 ].pageY;
					onPointerDownLon = lon;
					onPointerDownLat = lat;
				}
	 		},
	 		onDocumentTouchMove:function(event){
	 			if ( event.touches.length == 1 ) {
					event.preventDefault();
					lon = ( onPointerDownPointerX - event.touches[0].pageX ) * 0.1 + onPointerDownLon;
					lat = ( event.touches[0].pageY - onPointerDownPointerY ) * 0.1 + onPointerDownLat;
				}
	 		},
	 		animate:function(){
	 			requestAnimationFrame( animate );
				update();
	 		},
	 		update:function(){
	 			if ( isUserInteracting === false ) {
					lon += 0.1;
				}
				lat = Math.max( - 85, Math.min( 85, lat ) );
				phi = THREE.Math.degToRad( 90 - lat );
				theta = THREE.Math.degToRad( lon );
				target.x = 500 * Math.sin( phi ) * Math.cos( theta );
				target.y = 500 * Math.cos( phi );
				target.z = 500 * Math.sin( phi ) * Math.sin( theta );
				camera.lookAt( target );
				renderer.render( scene, camera );
	 		}
	 	},
	 	data(){
		    return{

		    }
		}
	 }
</script>